/** Parse MySQL `enum('a','b')` / `enum('a''b')` style definitions from COLUMN_TYPE. */
const parseEnumOptionsFromColumnType = (columnType: string): string[] | null => {
  const t = String(columnType || '').trim();
  const m = /^enum\s*\(([\s\S]*)\)\s*$/i.exec(t);
  if (!m) return null;
  const inner = m[1].trim();
  if (!inner) return null;
  const out: string[] = [];
  const re = /'((?:''|[^'])*)'/g;
  let match: RegExpExecArray | null;
  while ((match = re.exec(inner)) !== null) {
    out.push(match[1].replace(/''/g, "'"));
  }
  return out.length > 0 ? out : null;
};

const isAuditLikeColumnName = (colName: string) => {
  const n = colName.toLowerCase().replace(/["`]/g, '');
  const exact = new Set([
    'created_at',
    'updated_at',
    'deleted_at',
    'modified_at',
    'createdat',
    'updatedat',
    'deletedat',
    'modifiedat',
    'create_time',
    'update_time',
    'last_modified',
    'lastmodified',
    'created_date',
    'updated_date',
    'modified_date',
    'deleted_date',
  ]);
  if (exact.has(n)) return true;
  if (/(^|_)(created_at|updated_at|deleted_at|modified_at)$/.test(n)) return true;
  return false;
};

/** Typical surrogate PK column name — hide in grid/forms (not `user_id` / FKs). */
const hideBareIdColumnName = (colName: string) => /^id$/i.test(String(colName || '').trim());

const isAutoGeneratedIdColumn = (col: {
  name: string;
  extra?: string;
  columnType?: string;
  key?: string;
  type?: string;
  columnDefault?: string;
}) => {
  const name = String(col?.name || '').toLowerCase().trim();
  const extra = String(col?.extra || '').toLowerCase();
  const columnType = String(col?.columnType || '').toLowerCase();
  const key = String(col?.key || '').toUpperCase();
  const def = String(col?.columnDefault || '').toLowerCase();

  if (extra.includes('auto_increment')) return true;
  if (extra.includes('identity')) return true;
  if (columnType.includes('serial')) return true;
  if (columnType.includes('identity')) return true;
  if (def.includes('nextval')) return true;
  if (key === 'PRI' && name === 'id') return true;
  return false;
};

