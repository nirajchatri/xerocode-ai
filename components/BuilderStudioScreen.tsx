const normalizeDashboards = (incoming: BuilderDashboard[] | undefined): BuilderDashboard[] => {
  const list = Array.isArray(incoming) && incoming.length > 0 ? [...incoming] : defaultDashboardList();
  if (!list.some((d) => d.id === OVERALL_DASHBOARD_ID)) {
    return [{ id: OVERALL_DASHBOARD_ID, name: 'Dashboard' }, ...list];
  }
  return list.map((d) =>
    d.id === OVERALL_DASHBOARD_ID ? { ...d, name: 'Dashboard' } : d
  );
};

